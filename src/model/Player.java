package model;

public class Player {
    public int row;
    public int col;
    public int H;
    public int A;
    public int D;
    public int s;
    public int S;

    public Player(int row, int col, int H, int A, int D, int S) {
        this.row = row;
        this.col = col;
        this.H = H;
        this.A = A;
        this.D = D;
        this.s = S;
        this.S = S;
    }
}
